    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        Connection c = null;
        PreparedStatement pstmt = null;
        try {
            // Retrieve productID parameter from the request
            int productID = Integer.parseInt(request.getParameter("productID"));
            System.out.println(productID);
            // Establish database connection
            c = FruitDB.getConnect();

            // Define SQL query to delete the fruit with the given productID
            String sql = "DELETE FROM Products WHERE productID = ?";

            // Create PreparedStatement
            pstmt = c.prepareStatement(sql);

            // Set productID parameter for the PreparedStatement
            pstmt.setInt(1, productID);

            // Execute the query
            pstmt.executeUpdate();
            
            // Redirect back to the page where the delete request originated from
            response.sendRedirect("welcome.jsp");
        } catch (SQLException | NumberFormatException e) {
            e.printStackTrace(); // Print stack trace for debugging
        }
    }

